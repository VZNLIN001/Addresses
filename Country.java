public class Country{
	
	public String code;
	public String name;
	
	public String getCode(){
		return code;
	}
	
	public String getName(){
		return code;
	}
	
	public void setCode(String code){
		this.code = code;
	}
	
	public void setName(String name){
		this.name = name;
	}
	
	public String toString(){
		return name;
	}

}